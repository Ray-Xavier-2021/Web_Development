# Sequencing: indices
name = 'Xavier'

print(name[0]) # X
print(name[3]) # i

names = ['Ray', 'Xavier', 'Mystery']

print(names[0]) # Ray

# Get the length of sequence
print(f'The sequence has {len(names)} elements.') 

# Tuple: sequence of immutable/unchangeable values (Pair  of values)
coordinate = (10.0, 20.0)

print(coordinate)

